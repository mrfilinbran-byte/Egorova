# Ввод номера дня недели (1-7)
day_number = int(input("Введите номер дня недели (1-7): "))

# Словари для названий дней и режима работы
days = {
    1: "Понедельник",
    2: "Вторник",
    3: "Среда",
    4: "Четверг",
    5: "Пятница",
    6: "Суббота",
    7: "Воскресенье"
}

# Определяем рабочий (1-5) или выходной (6,7)
if 1 <= day_number <= 5:
    status = "Рабочий день"
    schedule = "8:00 - начало смены"
elif day_number in (6, 7):
    status = "Выходной"
    schedule = "Отдых"
else:
    # Обработка некорректного ввода
    print("Ошибка: номер дня должен быть от 1 до 7")
    exit()

# Получение названия дня
day_name = days.get(day_number, "Неизвестный день")

# Лаконичный форматированный вывод
print("\n=== Рабочий график ===")
print(f"День недели: {day_name}")
print(f"Режим: {status}")
print(f"Расписание: {schedule}")