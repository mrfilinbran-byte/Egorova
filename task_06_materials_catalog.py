# 1. Создать список из 5 материалов
materials = ["Древесина", "Сталь", "Бетон", "Стекло", "Пластик"]
print("Исходный список материалов:", materials)

# 2. Вывести первый, последний, средние элементы
print("\n=== Индексация и срезы ===")
print(f"Первый элемент: {materials[0]}")
print(f"Последний элемент: {materials[-1]}")

# Средние элементы (для списка из 5 элементов это индексы 1,2,3 или срез 1:4)
middle_elements = materials[1:4]  # со второго по четвёртый (индексы 1,2,3)
print(f"Средние элементы (срез 1:4): {middle_elements}")

# 3. Добавить 2 новых материала
materials.append("Керамика")
materials.append("Композит")
print("\nПосле добавления двух материалов:", materials)

# 4. Удалить второй элемент (индекс 1, так как нумерация с 0)
deleted = materials.pop(1)  # удаляем элемент с индексом 1 (второй по счёту)
print(f"Удалённый элемент: {deleted}")

# 5. Вывести итоговый список и длину
print("\n=== Итоговый каталог ===")
print(f"Итоговый список материалов: {materials}")
print(f"Длина списка (количество материалов): {len(materials)}")