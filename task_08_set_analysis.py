# 1. Создаём 3 списка материалов (могут повторяться внутри, но для множеств это не важно)
supplier1 = ["кирпич", "цемент", "песок", "гравий", "доски"]
supplier2 = ["цемент", "песок", "щебень", "арматура", "доски"]
supplier3 = ["песок", "гравий", "цемент", "краска", "доски"]

# Преобразуем в множества для удобства операций
set1 = set(supplier1)
set2 = set(supplier2)
set3 = set(supplier3)

print("Материалы подрядчика 1:", supplier1)
print("Материалы подрядчика 2:", supplier2)
print("Материалы подрядчика 3:", supplier3)

# 2. Все уникальные материалы (объединение всех трёх)
all_unique = set1 | set2 | set3

# 3. Общие для всех трёх (пересечение)
common_all = set1 & set2 & set3

# 4. Только у первого (материалы, которые есть в первом, но отсутствуют во втором и третьем)
only_first = set1 - (set2 | set3)

# 5. Ровно у двух (материалы, которые встречаются ровно в двух множествах)
# Вариант: (попарные пересечения) минус тройное пересечение
in_two = ((set1 & set2) | (set1 & set3) | (set2 & set3)) - common_all

# Вывод результатов
print("\n=== Результаты анализа ===")
print(f"Все уникальные материалы: {sorted(all_unique)}")
print(f"Общие для всех трёх: {sorted(common_all)}")
print(f"Только у первого подрядчика: {sorted(only_first)}")
print(f"Материалы ровно у двух подрядчиков: {sorted(in_two)}")